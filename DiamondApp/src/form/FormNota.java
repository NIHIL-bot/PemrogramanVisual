    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    DefaultTableModel model = (DefaultTableModel) txtTbltrx.getModel();
    String idNota = txtIdnota.getText();
    String kdBarang = kdBg.getText();
    String namaBarang = txtNamaBg.getText();
    int hargaJual = Integer.parseInt(txtHargaJual.getText());
    int qty = Integer.parseInt(txtQty.getText());
    int total = hargaJual * qty;

    model.addRow(new Object[]{
        idNota,
        kdBarang,
        namaBarang,
        hargaJual,
        qty,
        total

    });
    hitungTotalHarga();
    clearBarang();

    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
        Connection conn = koneksi.Koneksi.getKoneksi();
        hitungTotalHarga();
        PreparedStatement stmt = conn.prepareStatement(
              "INSERT INTO nota (id_nota, id_pelanggan, tgl_nota, total) VALUES (?, ?, ?, ?)");
        stmt.setString(1, txtIdnota.getText());
        stmt.setString(2, txtId.getText());
        Date tgl = (Date) txtTgl.getValue();
        stmt.setString(3, new SimpleDateFormat("yyyy-MM-dd").format(tgl));
        stmt.setInt(4, Integer.parseInt(txtTotalharga.getText()));
        stmt.executeUpdate();


        // Simpan detail transaksi
        DefaultTableModel model = (DefaultTableModel) txtTbltrx.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            PreparedStatement detailStmt = conn.prepareStatement(
            "INSERT INTO detail_nota (id_nota, kd_barang, nama_barang, harga_jual, qty, total) VALUES (?, ?, ?, ?, ?, ?)");
        detailStmt.setString(1, txtIdnota.getText());
        detailStmt.setString(2, model.getValueAt(i, 1).toString()); // kd_barang
        detailStmt.setString(3, model.getValueAt(i, 2).toString()); // nama_barang
        detailStmt.setInt(4, Integer.parseInt(model.getValueAt(i, 3).toString())); // harga_jual
        detailStmt.setInt(5, Integer.parseInt(model.getValueAt(i, 4).toString())); // qty
        detailStmt.setInt(6, Integer.parseInt(model.getValueAt(i, 5).toString())); // total
        detailStmt.executeUpdate();
        }

        JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
    } catch (SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Gagal simpan data: " + e.getMessage());
    }

    }//GEN-LAST:event_btnSimpanActionPerformed

