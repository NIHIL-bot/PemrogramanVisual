    private void hitungTotalHarga() {
        int totalHarga = 0;
    DefaultTableModel model = (DefaultTableModel) txtTbltrx.getModel();

    for (int i = 0; i < model.getRowCount(); i++) {
        int total = Integer.parseInt(model.getValueAt(i, 5).toString());
        totalHarga += total;
    }

    txtTotalharga.setText(String.valueOf(totalHarga));
    }
